    public void recorrer(NodoLista Nodo) {
        Stack<NodoLista> pila = new Stack();
        NodoLista p = Nodo;
        int tope = 0;
        do {
            if (p != null) {
                if (p.nArbol.tipo.equals(2)) {
                    Ent.addItem(p.nArbol.dato.toString());
                }
                pila.push(p);
                p = p.nArbol.hijos.ptr;
                tope++;
            } else {
                p = (NodoLista) pila.pop();
                p = p.link;
                tope--;
            }
        } while (tope != 0 || p != null);
    }

    public void dfs(Vertices p) {
        Stack<Vertices> pila = new Stack();
        Vertices o;
        NodoListaG q;
        int tope = 1;
        p.visitado = true;
        pila.push(p);
        do {
            p = pila.pop();
            tope--;
            if (p != null) {
                Vertext.append("," + p.dato.toString());
                q = p.dependencia.ptr;
                while (q != null) {
                    o = buscar(q.nGrafo.dato);
                    if (o.visitado != true) {
                        o.visitado = true;
                        pila.push(q.nGrafo);
                        tope++;
                    }
                    q = q.link;
                }
            }
        } while (p != null && tope != 0);
        p = L.ptr;
        while (p != null) {
            p.visitado = false;
            p = p.linkG;
        }
    }

